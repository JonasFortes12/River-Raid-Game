function DrawBarries(barrie){ // recebe a matrix da barreira específica (cenário)
    // this.barries = new Barries()

    for(let row in barrie){
        for(let column in barrie[row]){

            let tile = barrie[row][column] //bloquinho
            let tileSize = 5 //tamanho do bloquinho

        
            if(tile === 1){
                let positionX = column*tileSize
                let positionY = row*tileSize
                
                elementTile = newElement('div', 'tile')
                elementTile.style.left = `${positionX}%`
                elementTile.style.top = `${positionY}%`
                elementTile.style.width = `${tileSize}%`
                elementTile.style.height = `${tileSize}%`
                elementTile.style.position = "absolute"

                elementTile.style.backgroundColor = "green"

                gameArea.appendChild(elementTile)
                
            }
        }
    }
}

function SortBarries(barries){ // recebe o objeto que contem todas as barreiras (cenários)

    this.arrBarries = [ 
        barries.bifurcation,
        barries.zigzag,
        barries.midle,
        barries.toright,
        barries.toleft
    ]

    return this.arrBarries[Math.floor(Math.random()*this.arrBarries.length)] // retorna uma barreira aleatória



}

function Ship(){

    this.goingRight = false
    this.goingLeft = false
    
    this.element = newElement('img', 'ship')
    this.element.src = 'imgs/nave.png'
    this.element.style.right = "500%"

    // this.getX = () => parseInt(this.element.style.right.split('px')[0])
    // this.getX = () => this.element.style
    this.getX = () => parseInt(this.element.style.right.split('px')[0])
    this.setX = (x) => this.element.style.right = `${x}px`

    document.addEventListener("keydown", (event) =>{
        switch(event.key){
            case "ArrowLeft":
                this.goingLeft = true
                break
            
            case "ArrowRight":
                this.goingRight = true
                break
        }   
    });
    
    document.addEventListener("keyup", (event) =>{
        switch(event.key){
            case "ArrowLeft":
                this.goingLeft = false
                break
            
            case "ArrowRight":
                this.goingRight = false
                break
        }   
    });


    this.animate = () => {
        
        const motion = 10 //acrescimo de pixels por movimento
        let newXposition = this.getX()

        if(this.goingRight){
            newXposition = this.getX() - motion;
        }else if(this.goingLeft){
            newXposition = this.getX() + motion;
        }
        this.setX(newXposition)
    }

}

function RiverRaid(){

    const ship = new Ship()
    const barries = new Barries()
    DrawBarries(SortBarries(barries))
        

    gameArea.appendChild(ship.element)

    this.start = () => {

        const timer = setInterval(() =>{
            
            ship.animate()
            
            
        }, 20)

    }
}


